#include <iostream>
using namespace std;
int main(){
	int a = 5;      // pierwsza liczba
	int b = 3;      // druga liczba
	
	int suma = a + b;
	int roznica = a - b;
	int iloczyn = a * b;

	cout << "Liczba a: " << a << endl;
	cout << "Liczba b: " << b << endl;
	cout << "Suma: " << suma << endl;
	cout << "Różnica: " << roznica << endl;
	cout << "Iloczyn: " << iloczyn << endl;
	
	return 0;
}